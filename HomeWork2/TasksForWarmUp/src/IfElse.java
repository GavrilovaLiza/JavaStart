public class IfElse {
    public static void main(String[] args) {

        int a = 15;
        int b = 41;

        if(a < b){
            System.out.println("-1");
        }else if(a==b){
            System.out.println("0");
        }else if(a > b){
            System.out.println("+1");
        }
    }
}
