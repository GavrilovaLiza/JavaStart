public class CakesTwo {
    public static void main(String[] args) {

        int cakes = 10;

        System.out.println("I have " + cakes + " cakes!");

        for (int n = cakes - 1; n >=0; n--){
            System.out.println("I have eaten cake! " + n + " are left...");

        }
    }
}
